def find_nearest_mrt(lat: float, lon: float) -> tuple:
    """找到最近的捷運站，回傳 (站名, 距離km, 系統名)"""
    best = None
    best_dist = float("inf")
    for name, slat, slon, system in unique_stations:
        d = haversine(lat, lon, slat, slon)
        if d < best_dist:
            best_dist = d
            best = (name, d, system)
    return best


def main():
    # 載入現有 district_profiles
    profiles_path = DATA_DIR / "district_profiles.json"
    if profiles_path.exists():
        with open(profiles_path, "r", encoding="utf-8") as f:
            profiles = json.load(f)
    else:
        profiles = {}

    centers = get_district_centers()
    updated = 0

    for city, districts in profiles.items():
        for district, profile in districts.items():
            # 加入空屋率
            vr = VACANCY_RATES.get(city, {}).get(district)
            if vr is not None:
                profile["vacancy_rate"] = vr
                updated += 1

            # 加入最近捷運站
            center = centers.get((city, district))
            if center:
                nearest = find_nearest_mrt(center[0], center[1])
                if nearest:
                    name, dist_km, system = nearest
                    walk_min = round(dist_km / 0.08)  # 步行速度 ~4.8km/h = 0.08km/min
                    profile["nearest_mrt"] = {
                        "station": name,
                        "distance_km": round(dist_km, 2),
                        "walk_minutes": min(walk_min, 99),
                        "system": system,
                    }

    # 寫回
    with open(profiles_path, "w", encoding="utf-8") as f:
        json.dump(profiles, f, ensure_ascii=False, indent=2)

    print(f"更新完成：{updated} 個區域加入空屋率/捷運站資訊")
    print(f"輸出：{profiles_path}")


if __name__ == "__main__":
    main()
